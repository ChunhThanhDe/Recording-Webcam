import 'package:flutter/material.dart';
import 'package:flutter/services.dart';

import 'camera_page.dart';


void main() async {
  WidgetsFlutterBinding.ensureInitialized();
  SystemChrome.setEnabledSystemUIMode(SystemUiMode.manual, overlays: []);
